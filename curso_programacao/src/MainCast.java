
public class MainCast {
	public static void main(String[] args) {
		
		int a, b;
		double resultado;
		a = 5;
		b = 2;
		resultado = (double) a / b;
		System.out.println(resultado);
		
		
		System.out.println();
		System.out.println("============================================");
		System.out.println();
		
		double x;
		int z;
		x = 5.0;
		z = (int) x;
		System.out.println(b);
	}
}
