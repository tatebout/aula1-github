public class MainArea {
	public static void main(String[] args) {
		double b, B, h, area;
		b = 6.0;
		B = 8.0;
		h = 5.0;
		area = (b + B) / 2.0 * h;
		System.out.println(area);

		System.out.println();
		System.out.println("============================================");
		System.out.println();

		float bf, Bf, hf, areaf;
		bf = 6f;
		Bf = 8f;
		hf = 5f;
		areaf = (bf + Bf) / 2f * hf;
		System.out.println(areaf);
	}
}